// ── PII（個人情報）マスキング ──
// ノウハウキングは認証ゼロで全公開のため、送る前に個人情報をマスクする。
// 仕様書§10「起票内容に個人情報が混ざった場合の扱い」への対応。
// 完璧な検出は不可能なので「よくある明確なPIIパターン」を保守的に潰す方針。

/** 日本の電話番号・メール・長い数字列などを伏字化する */
export function maskPII(input: string): string {
  if (!input) return input;
  let s = input;

  // メールアドレス
  s = s.replace(/[\w.+-]+@[\w-]+\.[\w.-]+/g, "[メール]");

  // 日本の電話番号（市外局番ハイフン区切り／携帯／フリーダイヤル等）
  // 例: 03-1234-5678, 090-1234-5678, 0120-123-456, 0612345678
  s = s.replace(/0\d{1,4}[-(]?\d{1,4}[-)]?\d{3,4}/g, "[電話]");

  // 連続10桁以上の数字（口座・会員番号など）
  s = s.replace(/\d{10,}/g, "[番号]");

  // 郵便番号 〒123-4567 / 123-4567
  s = s.replace(/〒?\s?\d{3}-\d{4}/g, "[郵便番号]");

  return s;
}

/** PIIらしき文字列が含まれていそうかの簡易判定（ログ・注意喚起用） */
export function looksLikePII(input: string): boolean {
  if (!input) return false;
  return (
    /[\w.+-]+@[\w-]+\.[\w.-]+/.test(input) ||
    /0\d{1,4}[-(]?\d{1,4}[-)]?\d{3,4}/.test(input) ||
    /\d{10,}/.test(input)
  );
}
